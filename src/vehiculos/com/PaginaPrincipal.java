    private void b_loginLoginMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_b_loginLoginMouseClicked
        // TODO add your handling code here:
        File f = null;
        Scanner sc = null;
        boolean b = false;
        String usuario = tf_loginUsuario.getText();
        String pass = pf_loginContraseña.getText();
        try {
            f = new File("./usuarios.txt");
            sc = new Scanner(f);
            sc.useDelimiter(";");
            String nombre, nick, contra, pais, correo, ex, fecha, rol, ubi;
            while (sc.hasNext()) {
                nombre = sc.next();
                nick = sc.next();
                contra = sc.next();
                pais = sc.next();
                fecha = sc.next();
                correo = sc.next();
                ubi = sc.next();
                rol = sc.next();
                if (usuario.equals(nick)) {
                    if (pass.equals(contra)) {
                        new PaginaPrincipal().setVisible(true);
                        if (rol.equals("Ofertador")) {
                            
                            Ofertadores.setVisible(true);
                            b = true;
                            
                            CerrarOfertadores();
                            Login.setVisible(false);
                        } else if (rol.equals("Cliente")) {
                            jf_Cliente.setVisible(true);
                            b = true;
                        } else if (rol.equals("Administrador")) {
                            jf_Admin.setVisible(true);
                            b = true;
                        }
                    } else {
                        JOptionPane.showMessageDialog(this, "Contraseña incorrecta");
                    }
                }
            }
            if (b == false) {
                JOptionPane.showMessageDialog(this, "Usuario Incorrecto");
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
        sc.close();
    }//GEN-LAST:event_b_loginLoginMouseClicked

