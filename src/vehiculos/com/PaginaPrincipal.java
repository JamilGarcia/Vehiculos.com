    public void EliminarVehiculos() {
        try {
            int pos = tabla_AdVehiculos.getSelectedRow();
            DefaultTableModel tablaM = (DefaultTableModel) tabla_AdVehiculos.getModel();
            tablaM.removeRow(pos);
            tabla_AdVehiculos.setModel(tablaM);
            advh.cargarArchivo();
            advh.getVehi().remove(pos);
            advh.escribirArchivo();
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Seleccione una fila");
        }

    }

    public void ModificarVehiculos() {
        String marca, modelo, cilindraje, tipo, transmision;
        int año, precio;
        DefaultTableModel tablaM = (DefaultTableModel) tabla_AdVehiculos.getModel();
        try {
            Object[] newrow = {tabla_AdVehiculos.getSelectedRow()};
            EliminarVehiculos();
            tablaM.addRow(newrow);
            advh.cargarArchivo(); 
        } catch (Exception e) {
        }
    }

    public void VerVehiculos() {

        String marca, modelo, cilindraje, tipo, transmision;
        int año, precio;

        advh.cargarArchivo();
        ArrayList<Vehiculo> temp = advh.getVehi();
        DefaultTableModel tablaM = (DefaultTableModel) tabla_AdVehiculos.getModel();
        for (int i = 0; i < temp.size(); i++) {
            Object[] newrow = {
                marca = advh.getVehi().get(i).getMarca(),
                modelo = advh.getVehi().get(i).getModelo(),
                año = advh.getVehi().get(i).getAño(),
                cilindraje = advh.getVehi().get(i).getCilindraje(),
                tipo = advh.getVehi().get(i).getTipo(),
                transmision = advh.getVehi().get(i).getTransmision(),
                precio = advh.getVehi().get(i).getPrecio()
            };
            tablaM.addRow(newrow);
            tabla_AdVehiculos.setModel(tablaM);
        }

    }

